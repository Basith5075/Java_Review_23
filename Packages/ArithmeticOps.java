package tp;

public class ArithmeticOps {
    
   public int sum(int num1, int num2){
        int sum = num1 + num2 ; 
        return sum;
    }

    public int product(int num1, int num2){
        int prod = num1 * num2;

        return prod;
    }

    public double div(double num1, double num2){

        double div = num1 / num2;
        
        return div;
    }
}
